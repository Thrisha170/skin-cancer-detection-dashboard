# ---------------- LOAD MODEL ----------------
@st.cache_resource
def load_model():
    if not os.path.exists("skin_cancer_model.h5"):
        st.error("‚ùå skin_cancer_model.h5 not found")
        st.stop()
    return tf.keras.models.load_model("skin_cancer_model.h5")

model = load_model()

